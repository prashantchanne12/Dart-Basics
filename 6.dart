void main(){
  
  // Iterations
  
  
  // For Loop
  var sum = 0;
  
  for(var i=1; i<=5; i++){
    sum += i;
  }
  
  print(sum);
  
  
  // While Loop
  var a = 5;
  var b = 0;
  
  while(a!=0){
    b += a;
    a--;
  }
  
  print(b);
  
  // Do While loop
  
  var r = 0;
  
  do{
    print("Hello World!");
  }while(r!=0);
  
}